#include <iostream>
#include "HumanA.hpp"
#include "HumanB.hpp"

int main(void)
{
    {
        Weapon club = Weapon("crude spiked club");
        HumanA bob("Bob", club);
        bob.attack();
        club.setType("some other type of club");
        bob.attack();
    }
    {
        Weapon club = Weapon("crude spiked club");
        HumanB jim("Jim");
        jim.setWeapon(club);
        jim.attack();
        club.setType("some other type of club");
        jim.attack();
    }
    {
        Weapon club = Weapon();
        HumanA bob("Bob", club);
        bob.attack();
        club.setType("");
        bob.attack();
    }
    {
        Weapon club = Weapon();
        HumanB jim("jim");
        jim.setWeapon(club);
        jim.attack();
        club.setType("");
        jim.attack();
    }
    return 0;
}